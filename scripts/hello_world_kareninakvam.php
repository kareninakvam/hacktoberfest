// This is a simple "Hello, World!" example in PHP.
// It creates an HTML document with a title and a heading (<h1>) that displays "Hello, World!".
// PHP code is embedded within the HTML using the <?php  ?>tags.
<!DOCTYPE html>
<html lang="en">

<head>
    <title>Hello, World!</title>
</head>

<body>
    <h1><?php echo "Hello, World!"; ?></h1>
</body>

</html>
